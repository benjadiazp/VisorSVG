public class Ellipse {
  int cx;
  int cy;
  int rx;
  int ry;
  int strokewidth;
  String fill;
  String stroke;

  public Ellipse (int cx, int cy, int rx, int ry, int sw, String f, String s){
    this.cx = cx;
    this.cy = cy;
    this.rx = rx;
    this.ry = ry;
    strokewidth = sw;
    fill = f;
    stroke = s;

  }
}
