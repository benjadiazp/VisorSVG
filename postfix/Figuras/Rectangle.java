public class Rectangle
{
  int x;
  int y;
  int width;
  int height;
  String fill;
  int strokewidth;
  String stroke;

  public Rectangle (int x, int y, int w, int h, String f, int sw, String s)
  {
    this.x = x;
    this.y = y;
    width = w;
    height = h;
    fill = f;
    strokewidth = sw;
    stroke = s;
  }
}
